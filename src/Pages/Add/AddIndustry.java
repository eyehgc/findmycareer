    private void btnAddIndustryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddIndustryActionPerformed
        // TODO add your handling code here:
        try
        {
            manageIndustries = new ManageIndustries();
            String industryName = txtIndustryName.getText();
            
            manageIndustries.addIndustry(industryName);
            if (manageIndustries.getSuccesful())
                txtIndustryName.setText("");
            labelMessage.setText(manageIndustries.getMessage());
        }
        catch (Exception ex)
        {
           btnRetry.setVisible(true);
           labelConnectionError.setVisible(true);
           btnAddIndustry.setEnabled(false);
        }
        
        
    }//GEN-LAST:event_btnAddIndustryActionPerformed

