    private void txtIndustryNameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIndustryNameKeyReleased
        // TODO add your handling code here:
        int length = txtIndustryName.getText().length();
        if (length > 35)
        {
            labelValidationMessage.setForeground(new java.awt.Color(204, 0, 0));
            labelValidationMessage.setText("Industry name too long (35 chars max)");
            labelValidationMessage.setVisible(true);
            btnAddIndustry.setEnabled(false);
        }
        else if (length < 2)
        {
            labelValidationMessage.setForeground(new java.awt.Color(204, 0, 0));
            labelValidationMessage.setText("Industry name too short");
            labelValidationMessage.setVisible(true);
            btnAddIndustry.setEnabled(false);
        }
        else
        {
            String industryEntered = txtIndustryName.getText();
            try
            {
                industries = new Industries();
                if(industries.checkIfIndustryExists(industryEntered) == false)
                {
                    labelValidationMessage.setForeground(new java.awt.Color(204, 0, 0));
                    labelValidationMessage.setText("Industry already exists");
                    labelValidationMessage.setVisible(true);
                    btnAddIndustry.setEnabled(false);
                }
                else
                {
                    labelValidationMessage.setForeground(new java.awt.Color(0, 204, 51));
                    labelValidationMessage.setText("Industry Name Valid");
                    labelValidationMessage.setVisible(true);
                    btnAddIndustry.setEnabled(true);
                }
            }
            catch(Exception ex)
            {
                
            }
        }
    }//GEN-LAST:event_txtIndustryNameKeyReleased

