    private void cbxIndustryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxIndustryActionPerformed
        // TODO add your handling code here:
        String StrIndustry = (String)cbxIndustry.getSelectedItem();
        cbxDiscipline.removeAllItems();

        try
        {
            industries = new Industries();
            disciplines = new Disciplines();
            int industryID = industries.getIndustryIDByName(StrIndustry);
                disciplinesRS = disciplines.listDisciplinesByIndustryID(industryID);
                while(disciplinesRS.next())
                    {
                         String disciplineName = disciplinesRS.getString("disciplineName");
                         cbxDiscipline.addItem(disciplineName);
                    }
               cbxDiscipline.setEnabled(true);
               txtUpdateDiscipline.setEnabled(false);
        } catch (Exception ex)
        {
            cbxIndustry.setEnabled(false);
            cbxDiscipline.setEnabled(false);
            txtUpdateDiscipline.setEnabled(false);
        }
        
    }//GEN-LAST:event_cbxIndustryActionPerformed

