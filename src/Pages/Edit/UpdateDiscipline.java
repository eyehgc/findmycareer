    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        
        try
        {
            disciplines = new Disciplines();
            String disciplineName = (String)cbxDiscipline.getSelectedItem();
            String updateDisciplineName = txtUpdateDiscipline.getText();
            
            disciplines.updateDiscipline(updateDisciplineName, disciplineName);
            if (disciplines.getSuccesful())
                txtUpdateDiscipline.setText("");
                labelMessage.setText(disciplines.getMessage());

        }
        catch (Exception ex)
        {
           btnUpdate.setEnabled(false);
           System.out.println("Error, error, error!!!: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

