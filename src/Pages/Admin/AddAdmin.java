    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        passwordValidation = new PasswordValidation();
        
        String firstName = (String) txtFirstName.getText();
        String lastName= (String) txtLastName.getText();
        String email=(String) txtEmail.getText();
        String password1 = txtPassword1.getText();
        String password2 = txtPassword2.getText();
        passwordValidation.passwordValidation(password1, password2); 
        String message = passwordValidation.getMessage();

         try
        {
            admins = new ManageAdmins();
            
            if (firstName.isEmpty()){
                labelMessage.setText("Error: You must enter a first name.");
                labelMessage.setVisible(true);
             }
           
            else if (lastName.isEmpty()){
                labelMessage.setText("Error: You must enter a last name.");
                labelMessage.setVisible(true);
             }
            
            else if (email.isEmpty()){
                labelMessage.setText("Error: You must enter an email address.");
                labelMessage.setVisible(true);
             }
            else if(firstName.length() > 50 || lastName.length()>50)
            {
                labelMessage.setText("Error: First and last names must be less than 50 characters.");
                labelMessage.setVisible(true);
                txtLastName.setEnabled(false);
                txtEmail.setEnabled(false);
                txtPassword1.setEnabled(false);
                txtPassword2.setEnabled(false);
                btnCreate.setEnabled(false);
            }
            else if (admins.queryDBForEmail(email) == false)
            {
                labelMessage.setText("Error: This email is already registered.");
                labelMessage.setVisible(true);
            }
            else if (validEmailFormat(email)==false){
                labelMessage.setText("Error: Please enter a valid email address.");
            }
            else if(passwordValidation.checkValid()==false){
                labelMessage.setText(message);
            }
            else
            {
                admins = new ManageAdmins();
                admins.addAdmin(email, password2, firstName, lastName);
                labelMessage.setText("'" + firstName + " "+ lastName + "' has been added as an admin.");
                labelMessage.setVisible(true);
                
            }
            
        }
        catch(Exception ex)
        {
            labelMessage.setText("Error: Something went wrong.");
        }
    }//GEN-LAST:event_btnCreateActionPerformed

