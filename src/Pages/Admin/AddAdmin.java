    public static boolean validEmailFormat(String email){
        String emailFormat = "^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
		+ "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$";
        if (email.matches(emailFormat)){ 
            return true;
        } else {
            return false;
        }
    }    

