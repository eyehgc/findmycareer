    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String courseName = (String)comboCourseName.getSelectedItem();
        
        int prompt = JOptionPane.showConfirmDialog(null, "Deleting '" + courseName + "' will \n"
                + "also delete all career outcomes linked to the course \n\n Do you want to continue?", "Confirm",
        JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        
        if (prompt == JOptionPane.YES_OPTION)
        {
            try
            {
                courses = new Courses();
                courses.removeCourse(courseName);
                labelMessage.setForeground(new java.awt.Color(255, 255, 255));
                labelMessage.setText("'" + courseName + "' has been deleted.");
                labelMessage.setVisible(true);
            }
            catch(Exception ex)
            {
                labelMessage.setForeground(new java.awt.Color(204, 0, 0));
                labelMessage.setText("Error: Unable to connect to the database");
                labelMessage.setVisible(true);
            }
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

