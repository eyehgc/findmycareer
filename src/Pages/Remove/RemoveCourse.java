    private void btnRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshActionPerformed
        // TODO add your handling code here:
        labelMessage.setVisible(false);
        btnDelete.setEnabled(false);
        labelRefreshMessage.setVisible(false);
        
        try
        {
            courses = new Courses();
            coursesRS = courses.listAllCourses();
            comboCourseName.removeAllItems();
            comboCourseName.addItem("- Select -");
            while(coursesRS.next())
            {
                String courseName = coursesRS.getString("courseName");
                comboCourseName.addItem(courseName);
            }
            labelMessage.setForeground(new java.awt.Color(255, 255, 255));
            labelRefreshMessage.setText("The list has been refreshed (click to hide)");
            labelRefreshMessage.setVisible(true);
        }
        catch(Exception ex)
        {
            labelRefreshMessage.setForeground(new java.awt.Color(204, 0, 0));
            labelRefreshMessage.setText("Error connecting to the database.");
            labelRefreshMessage.setVisible(true);
        }
    }//GEN-LAST:event_btnRefreshActionPerformed

